// ✅ ############## `continue` & `break` Statemenet ############## ✅ //

    // These 2 statements help to control the flow of loop.

    // ✅ ############## break Statement ############## ✅ //
        // The `break` statement immediately exits the loop, regardless of the loop condition.

        // Eg:
            for(let i=0; i<=5; i++) {
                if(i === 3) {
                    break;
                }

                console.log(i);
            }

            // Output:
                // 1
                // 2

    // ✅ ############## break Statement ############## ✅ //


    // ✅ ############## continue Statement ############## ✅ //
        // The `continue` statement skips the current iteration and jumps to the next iteration of the loop.

        // Eg:
            for (let i = 1; i <= 5; i++) {
                if (i === 3) {
                    continue;
                }
                console.log(i);
            }

            // Output:
                // 1
                // 2
                // 4
                // 5

    // ✅ ############## continue Statement ############## ✅ //

