// ✅ ############## Comparison Operators ############## ✅ //

    // Comparison Operators used to compare 2 values or expressions which returns boolean value(true or false).

    // == (Equal to)
    // === (Equal to & type)
    // != (Not Equal to)
    // !== (Not Equal to & type)
    // < (less than)
    // > (greater than)
    // <= (less than | equal to)
    // >= (greater than | equal to)

    
    // Eg:
        let x = 5;
        let y = 6;

        console.log("x == y", x == y);      // false
        console.log("x != y", x != y);      // true


        y = "5";

        console.log("x === y", x === y);    // false
        console.log("x !== y", x !== y);    // true

        y = 7;

        console.log("x < y", x < y)         // true
        console.log("x > y", x > y)         // false

// ✅ ############## Comparison Operators ############## ✅ //